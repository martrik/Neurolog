import UIKit

extension UIColor {
    class func appLightBlue() -> UIColor {
        return UIColor(red: 8/255.0, green: 141/255.0, blue: 253/255.0, alpha: 1.0)
    }
    
    class func appDarkBlue() -> UIColor {
        return UIColor(red: 22/255.0, green: 38/255.0, blue: 191/255.0, alpha: 1.0)
    }
    
    class func appOrange() -> UIColor {
        return UIColor(red: 253/255.0, green: 158/255.0, blue: 8/255.0, alpha: 1.0)
    }
    
    class func appViolet() -> UIColor {
        return UIColor(red: 112/255.0, green: 8/255.0, blue: 253/255.0, alpha: 1.0)
    }
    
    class func appLightGreen() -> UIColor {
        return UIColor(red: 0/255.0, green: 222/255.0, blue: 37/255.0, alpha: 1.0)
    }
    
    class func appGreen() -> UIColor {
        return UIColor(red: 7/255.0, green: 158/255.0, blue: 0/255.0, alpha: 1.0)
    }
    
    class func appBrown() -> UIColor {
        return UIColor(red: 188/255.0, green: 133/255.0, blue: 0/255.0, alpha: 1.0)
    }
    
    class func appRed() -> UIColor {
        return UIColor(red: 255/255.0, green: 66/255.0, blue: 0/255.0, alpha: 1.0)
    }
    
}